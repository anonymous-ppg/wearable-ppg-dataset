# encoding=utf-8
"""
data_preprocess_multisite.py
-----------------------------
Loads pre-aligned 4-device PPG data for a single participant.

Input file (inside each participant's folder):
    aligned_4device.npz
        ppg_earring  : (W, L)   green PPG from earring
        ppg_ring     : (W, L)   green PPG from ring
        ppg_watch    : (W, L)   green PPG from watch
        ppg_necklace : (W, L)   green PPG from necklace
        hr_gt        : (W,)     ground-truth heart rate in bpm
        t0_ms        : (W,)     window start timestamps (ms)

    W = number of aligned windows, L = original window length

    This file is generated by running aligned_4device.py first.

Processing:
    - Resample L → 200 samples (25 Hz × 8 s) per channel
    - Per-window z-score normalisation per channel
    - Stack 4 channels → X: (W, 200, 4)
    - Filter out windows with NaN or HR outside [BPM_MIN, BPM_MAX]
    - y: raw HR in bpm (float32) — regression target, no integer offset

Model input shape (after DataLoader):
    sample : (B, 200, C)   — models permute internally to (B, C, 200)
                              C = 4 (multi-device) or 1 (single-device)
    target : (B,)          — bpm float
"""

import os
import numpy as np
import scipy.signal
import torch
from torch.utils.data import DataLoader

from data_preprocess.data_preprocess_utils import train_val_split
from data_preprocess.base_loader import base_loader


BPM_MIN     = 30
BPM_MAX     = 210
TARGET_LEN  = 200    # 25 Hz × 8 s
DEVICE_KEYS = ['ppg_earring', 'ppg_ring', 'ppg_watch', 'ppg_necklace']


# ── Participant discovery ─────────────────────────────────────────────────────

def discover_multisite_participants(data_dir: str) -> list:
    """
    Scan data_dir for participant subfolders that contain aligned_4device.npz.

    Returns a sorted list of folder names, e.g. ["P1", "P2", ..., "P22"].
    Run aligned_4device.py first to generate these files.
    """
    if not os.path.isdir(data_dir):
        raise FileNotFoundError(f"data_dir not found: {data_dir}")

    participants = []
    for name in sorted(os.listdir(data_dir)):
        folder     = os.path.join(data_dir, name)
        align_file = os.path.join(folder, "aligned_4device.npz")
        if os.path.isdir(folder) and os.path.exists(align_file):
            participants.append(name)
        elif os.path.isdir(folder):
            print(f"  Skipping '{name}': no aligned_4device.npz found. "
                  f"Run aligned_4device.py first.")
    return participants


# ── Load one participant ──────────────────────────────────────────────────────

def load_domain_data(participant_id: str, data_dir: str, single_device=None):
    """
    Load and preprocess PPG data for one participant.

    Args:
        participant_id : participant folder name, e.g. "P1"
        data_dir       : root directory containing participant subfolders
        single_device  : None  → load all 4 device channels  (X shape: N, 200, 4)
                         0-3   → load only that one device   (X shape: N, 200, 1)
                                 0=earring 1=ring 2=watch 3=necklace

    Returns:
        X : np.ndarray, shape (N, 200, C), float32   — C is 4 or 1
        y : np.ndarray, shape (N,),        float32   — heart rate in bpm
    """
    path = os.path.join(data_dir, participant_id, "aligned_4device.npz")

    if not os.path.exists(path):
        raise FileNotFoundError(
            f"aligned_4device.npz not found for participant '{participant_id}' "
            f"at {path}. Run aligned_4device.py first."
        )

    # pick which device keys to load
    if single_device is None:
        keys = DEVICE_KEYS
        mode_str = "all 4 devices"
    else:
        keys = [DEVICE_KEYS[single_device]]
        mode_str = f"single device ({keys[0]})"

    print(f"  Loading {participant_id} | {mode_str}")

    data  = np.load(path, allow_pickle=True)
    hr_gt = data['hr_gt'].astype(np.float32)   # (W,)

    # ── load and resample each channel
    channels = []
    for key in keys:
        ppg = data[key].astype(np.float32)                     # (W, L)
        ppg = scipy.signal.resample(ppg, TARGET_LEN, axis=1)   # (W, 200)
        ppg = ppg.astype(np.float32)
        channels.append(ppg)

    # align lengths (should already match, but be safe)
    n        = min(len(hr_gt), *[len(c) for c in channels])
    hr_gt    = hr_gt[:n]
    channels = [c[:n] for c in channels]

    # ── filter invalid windows
    valid = (
        np.isfinite(hr_gt) &
        (hr_gt >= BPM_MIN) &
        (hr_gt <= BPM_MAX) &
        np.stack([~np.isnan(c).any(axis=1) for c in channels], axis=0).all(axis=0)
    )
    hr_gt    = hr_gt[valid]
    channels = [c[valid] for c in channels]

    # ── per-window z-score normalisation per channel
    normed = []
    for c in channels:
        c = (c - c.mean(axis=1, keepdims=True)) / (c.std(axis=1, keepdims=True) + 1e-6)
        normed.append(c)

    # ── stack into (N, 200, C)
    X = np.stack(normed, axis=2).astype(np.float32)

    # regression target: raw bpm, no integer offset
    y = hr_gt.astype(np.float32)

    print(f"    → {len(y)} valid windows, HR range {hr_gt.min():.0f}–{hr_gt.max():.0f} bpm")
    return X, y


def load_domain_data_subset(participant_id, data_dir, device_indices):
    """
    Load and preprocess a SUBSET of devices for one participant.

    Args:
        participant_id : participant folder name, e.g. "P1"
        data_dir       : root directory containing participant subfolders
        device_indices : list of int, e.g. [0, 1] for earring + ring
                         0=earring 1=ring 2=watch 3=necklace

    Returns:
        X : np.ndarray, shape (N, 200, len(device_indices)), float32
        y : np.ndarray, shape (N,), float32
    """
    path = os.path.join(data_dir, participant_id, "aligned_4device.npz")
    if not os.path.exists(path):
        raise FileNotFoundError(
            f"aligned_4device.npz not found for participant '{participant_id}' at {path}."
        )

    keys = [DEVICE_KEYS[i] for i in device_indices]
    print(f"  Loading {participant_id} | devices: {[k.replace('ppg_', '') for k in keys]}")

    data  = np.load(path, allow_pickle=True)
    hr_gt = data['hr_gt'].astype(np.float32)

    channels = []
    for key in keys:
        ppg = data[key].astype(np.float32)
        ppg = scipy.signal.resample(ppg, TARGET_LEN, axis=1).astype(np.float32)
        channels.append(ppg)

    n        = min(len(hr_gt), *[len(c) for c in channels])
    hr_gt    = hr_gt[:n]
    channels = [c[:n] for c in channels]

    valid = (
        np.isfinite(hr_gt) & (hr_gt >= BPM_MIN) & (hr_gt <= BPM_MAX) &
        np.stack([~np.isnan(c).any(axis=1) for c in channels], axis=0).all(axis=0)
    )
    hr_gt    = hr_gt[valid]
    channels = [c[valid] for c in channels]

    normed = []
    for c in channels:
        c = (c - c.mean(axis=1, keepdims=True)) / (c.std(axis=1, keepdims=True) + 1e-6)
        normed.append(c)

    X = np.stack(normed, axis=2).astype(np.float32)
    y = hr_gt.astype(np.float32)
    print(f"    → {len(y)} valid windows, HR range {hr_gt.min():.0f}–{hr_gt.max():.0f} bpm")
    return X, y


# ── Dataset ───────────────────────────────────────────────────────────────────

class MultisiteDataset(base_loader):
    """
    PyTorch Dataset for multi-device PPG regression.

    __getitem__ returns:
        sample : torch.float32, shape (200, C)
        target : torch.float32, scalar bpm value
        domain : int (participant index, for reference only)
    """
    def __init__(self, X, y, domain_idx):
        domains = np.full(len(y), domain_idx, dtype=np.int64)
        super().__init__(X, y, domains)

    def __getitem__(self, index):
        sample = self.samples[index]
        target = self.labels[index]
        domain = self.domains[index]

        sample = torch.tensor(sample, dtype=torch.float32)   # (200, C)
        target = torch.tensor(target, dtype=torch.float32)   # scalar
        return sample, target, domain


# ── Train / val / test split ──────────────────────────────────────────────────

def prep_domains_subject_sp(args):
    """
    Leave-one-subject-out split.

    - args.target_domain : participant ID string to hold out as test, e.g. "P3"
    - All other participants are pooled for training, then split 80/20 into train/val.
    - The held-out participant is used entirely as the test set.
    """
    all_ids = discover_multisite_participants(args.data_dir)

    if not all_ids:
        raise FileNotFoundError(
            f"No participants with aligned_4device.npz found in '{args.data_dir}'. "
            f"Run aligned_4device.py first."
        )

    if args.target_domain not in all_ids:
        raise ValueError(
            f"target_domain '{args.target_domain}' not found. "
            f"Available participants: {all_ids}"
        )

    source_ids = [pid for pid in all_ids if pid != args.target_domain]

    # ── pool all source participants
    X_list, y_list = [], []
    for pid in source_ids:
        X, y = load_domain_data(pid, args.data_dir,
                                single_device=args.single_device)
        X_list.append(X)
        y_list.append(y)

    X_all = np.concatenate(X_list, axis=0)
    y_all = np.concatenate(y_list, axis=0)
    d_all = np.zeros(len(y_all), dtype=np.int64)  # domain array, for base_loader compat

    # ── train / val split (80 / 20)
    X_train, X_val, y_train, y_val, d_train, d_val = train_val_split(
        X_all, y_all, d_all, split_ratio=args.split_ratio
    )

    # ── test: held-out participant
    X_test, y_test = load_domain_data(args.target_domain, args.data_dir,
                                      single_device=args.single_device)

    # ── DataLoaders
    train_set = MultisiteDataset(X_train, y_train, domain_idx=0)
    val_set   = MultisiteDataset(X_val,   y_val,   domain_idx=0)
    test_set  = MultisiteDataset(X_test,  y_test,  domain_idx=0)

    train_loader = DataLoader(train_set, batch_size=args.batch_size,
                              shuffle=True,  drop_last=False)
    val_loader   = DataLoader(val_set,   batch_size=args.batch_size,
                              shuffle=False, drop_last=False)
    test_loader  = DataLoader(test_set,  batch_size=args.batch_size,
                              shuffle=False, drop_last=False)

    print(f"  Split — Train: {len(train_set)}  Val: {len(val_set)}  "
          f"Test: {len(test_set)} windows")

    return [train_loader], val_loader, test_loader


# ── Entry point ───────────────────────────────────────────────────────────────

def prep_multisite(args):
    if args.cases == 'subject_val':
        return prep_domains_subject_sp(args)
    else:
        raise ValueError(f"Unknown args.cases: '{args.cases}'. Expected 'subject_val'.")
