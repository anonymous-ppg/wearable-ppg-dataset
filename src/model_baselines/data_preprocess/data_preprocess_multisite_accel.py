# encoding=utf-8
"""
data_preprocess_multisite_accel.py
----------------------------------
Multisite data loader supporting 8 channels (4 devices × green + accel_z).

Reads aligned_8channel_accel.npz (generated by aligned_4device_accel.py).

Single-device runs use 2 channels (green + accel_z of that device).
Full multisite uses all 8.
"""

import os
import numpy as np
import scipy.signal
import torch
from torch.utils.data import DataLoader

from data_preprocess.data_preprocess_utils import train_val_split
from data_preprocess.base_loader import base_loader


BPM_MIN     = 30
BPM_MAX     = 210
TARGET_LEN  = 200

DEVICE_NAMES = ['earring', 'ring', 'watch', 'necklace']

DEVICE_KEYS = [
    'ppg_earring_green',  'ppg_earring_accel',
    'ppg_ring_green',     'ppg_ring_accel',
    'ppg_watch_green',    'ppg_watch_accel',
    'ppg_necklace_green', 'ppg_necklace_accel',
]

DEVICE_TO_CHANNELS = {
    0: [0, 1],   # earring
    1: [2, 3],   # ring
    2: [4, 5],   # watch
    3: [6, 7],   # necklace
}


def discover_multisite_participants(data_dir):
    if not os.path.isdir(data_dir):
        raise FileNotFoundError(f"data_dir not found: {data_dir}")
    participants = []
    for name in sorted(os.listdir(data_dir)):
        folder = os.path.join(data_dir, name)
        align_file = os.path.join(folder, "aligned_8channel_accel.npz")
        if os.path.isdir(folder) and os.path.exists(align_file):
            participants.append(name)
    return participants


def load_domain_data(participant_id, data_dir, single_device=None):
    path = os.path.join(data_dir, participant_id, "aligned_8channel_accel.npz")
    if not os.path.exists(path):
        raise FileNotFoundError(
            f"aligned_8channel_accel.npz not found at {path}. "
            f"Run aligned_4device_accel.py first."
        )

    if single_device is None:
        keys = DEVICE_KEYS
        mode_str = "all 4 devices (8 channels: green+accel_z)"
    else:
        chan_indices = DEVICE_TO_CHANNELS[single_device]
        keys = [DEVICE_KEYS[i] for i in chan_indices]
        mode_str = f"single device {DEVICE_NAMES[single_device]} (green+accel_z)"

    print(f"  Loading {participant_id} | {mode_str}")

    data  = np.load(path, allow_pickle=True)
    hr_gt = data['hr_gt'].astype(np.float32)

    channels = []
    for key in keys:
        ppg = data[key].astype(np.float32)
        ppg = scipy.signal.resample(ppg, TARGET_LEN, axis=1).astype(np.float32)
        channels.append(ppg)

    n        = min(len(hr_gt), *[len(c) for c in channels])
    hr_gt    = hr_gt[:n]
    channels = [c[:n] for c in channels]

    valid = (
        np.isfinite(hr_gt) & (hr_gt >= BPM_MIN) & (hr_gt <= BPM_MAX) &
        np.stack([~np.isnan(c).any(axis=1) for c in channels], axis=0).all(axis=0)
    )
    hr_gt    = hr_gt[valid]
    channels = [c[valid] for c in channels]

    normed = []
    for c in channels:
        c = (c - c.mean(axis=1, keepdims=True)) / (c.std(axis=1, keepdims=True) + 1e-6)
        normed.append(c)

    X = np.stack(normed, axis=2).astype(np.float32)
    y = hr_gt.astype(np.float32)

    print(f"    → {len(y)} valid windows, X shape {X.shape}")
    return X, y


class MultisiteAccelDataset(base_loader):
    def __init__(self, X, y, domain_idx):
        domains = np.full(len(y), domain_idx, dtype=np.int64)
        super().__init__(X, y, domains)

    def __getitem__(self, index):
        sample = self.samples[index]
        target = self.labels[index]
        domain = self.domains[index]

        sample = torch.tensor(sample, dtype=torch.float32)
        target = torch.tensor(target, dtype=torch.float32)
        return sample, target, domain


def prep_domains_subject_sp(args):
    all_ids = discover_multisite_participants(args.data_dir)
    if not all_ids:
        raise FileNotFoundError(f"No aligned_8channel_accel.npz in '{args.data_dir}'.")
    if args.target_domain not in all_ids:
        raise ValueError(f"target_domain '{args.target_domain}' not in {all_ids}")

    source_ids = [pid for pid in all_ids if pid != args.target_domain]

    X_list, y_list = [], []
    for pid in source_ids:
        X, y = load_domain_data(pid, args.data_dir, single_device=args.single_device)
        X_list.append(X)
        y_list.append(y)

    X_all = np.concatenate(X_list, axis=0)
    y_all = np.concatenate(y_list, axis=0)
    d_all = np.zeros(len(y_all), dtype=np.int64)

    X_train, X_val, y_train, y_val, _, _ = train_val_split(
        X_all, y_all, d_all, split_ratio=args.split_ratio
    )

    X_test, y_test = load_domain_data(args.target_domain, args.data_dir,
                                      single_device=args.single_device)

    train_set = MultisiteAccelDataset(X_train, y_train, domain_idx=0)
    val_set   = MultisiteAccelDataset(X_val,   y_val,   domain_idx=0)
    test_set  = MultisiteAccelDataset(X_test,  y_test,  domain_idx=0)

    train_loader = DataLoader(train_set, batch_size=args.batch_size,
                              shuffle=True, drop_last=False)
    val_loader   = DataLoader(val_set, batch_size=args.batch_size,
                              shuffle=False, drop_last=False)
    test_loader  = DataLoader(test_set, batch_size=args.batch_size,
                              shuffle=False, drop_last=False)

    print(f"  Split — Train: {len(train_set)}  Val: {len(val_set)}  Test: {len(test_set)}")
    return [train_loader], val_loader, test_loader
